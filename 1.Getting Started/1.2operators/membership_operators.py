# in -> Results to True if a value is in the specified sequence and False otherwise
#not in -> Results to True , if a value is not in the specified sequence and false otherwise

var1 = 'hello world'  #string
var2 = {1:'a',2:'b'} #dictionary
print('h' in var1)
print('hello' not in var2)
print(1 in var2)
print('a' in var2)

